package com.github.diplombmstu.matrix_trsansposition_cipher.cipher.impl;

/**
 * Transposition simple matrix key
 */
public class MatrixKey
{
    private int m;
    private int s;
    private int p;

    public int getM()
    {
        return m;
    }

    public void setM(int m)
    {
        this.m = m;
    }

    public int getS()
    {
        return s;
    }

    public void setS(int s)
    {
        this.s = s;
    }

    public int getP()
    {
        return p;
    }

    public void setP(int p)
    {
        this.p = p;
    }
}
